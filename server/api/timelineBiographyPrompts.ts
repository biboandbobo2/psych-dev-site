export function buildSimpleBiographyFactExtractionPrompt(params: {
  articleTitle: string;
  extract: string;
  focusHint?: string;
  factLimit?: number;
}) {
  return `Извлеки ВСЕ биографические факты из статьи о ${params.articleTitle}.
${params.focusHint?.trim() ? `\nФОКУС ЭТОГО ПРОХОДА\n${params.focusHint.trim()}\n` : ''}

ЗАДАЧА
Верни JSON-массив объектов. Каждый объект — один факт из жизни человека.

ФОРМАТ
[
  {"year": 1828, "text": "Родился в Ясной Поляне", "category": "birth", "sphere": "family"},
  {"year": 1844, "text": "Поступил в Казанский университет", "category": "education", "sphere": "education"},
  ...
]

ПОЛЯ
- year: число (год события). ОБЯЗАТЕЛЬНО определи год — точный или приблизительный по контексту. null — только если абсолютно невозможно определить.
- text: строка, 5-15 слов. Конкретное описание факта (не общие фразы вроде "Учёба" или "Публикация").
- category: одно из: birth, education, move, publication, career, family, friends, health, conflict, award, project, death, other
- sphere: одно из: education, career, creativity, family, health, friends, place, finance, hobby, other

МЕТОД РАБОТЫ
Читай статью последовательно от начала до конца. Для КАЖДОГО десятилетия жизни человека извлеки не менее 5 фактов. Не останавливайся на середине — поздние годы так же важны, как ранние.

ПРАВИЛА
- Извлеки ВСЕ конкретные события из текста. Ожидаемое количество — от 60 до 150 фактов.
- Один факт = одно конкретное событие. НЕ пиши обобщения типа «занимался физиологией» или «был известен» — только конкретные события с датой.
- НЕ извлекай характеристики из энциклопедического лида («X — русский писатель», «основал школу», «создал теорию»). Только конкретные датированные события.
- НЕ дублируй одно и то же событие разными формулировками.
- Включай: рождение, смерть, семью, образование, произведения, награды, переломы, конфликты, дружбы, переезды, болезни, хобби.
- Рождение КАЖДОГО ребёнка — отдельный факт.
- КАЖДУЮ награду, звание, членство в академии — отдельный факт.
- Научные открытия — как конкретные датированные события (доклад, публикация, эксперимент).
- Включай конкретные имена людей, организаций, мест и произведений.
- НЕ выдумывай факты, которых нет в тексте.

ТЕКСТ СТАТЬИ
${params.extract}

Верни ТОЛЬКО JSON-массив, без markdown, без пояснений.`;
}

export function buildBiographyGapFillingPrompt(params: {
  articleTitle: string;
  extract: string;
  existingFacts: string[];
  undatedFacts?: string[];
}) {
  const existingBlock = params.existingFacts.map((f, i) => `${i + 1}. ${f}`).join('\n');

  const hasUndated = params.undatedFacts && params.undatedFacts.length > 0;

  const undatedBlock = hasUndated
    ? params.undatedFacts!.map((f, i) => `U${i + 1}. ${f}`).join('\n')
    : '';

  return `Ты — второй проход извлечения фактов из статьи о ${params.articleTitle}.

Первый проход уже нашёл следующие ДАТИРОВАННЫЕ факты:
${existingBlock}
${hasUndated ? `
═══════════════════════════════════════
ЗАДАЧА 1 (ГЛАВНАЯ): ДАТИРОВАНИЕ
═══════════════════════════════════════
Следующие факты были извлечены БЕЗ года. Для КАЖДОГО из них определи год по тексту статьи и включи в ответ с полем "year". Если факт дублирует уже датированный — пропусти его.

${undatedBlock}
` : ''}
═══════════════════════════════════════
ЗАДАЧА ${hasUndated ? '2' : '1'}: ПРОПУЩЕННЫЕ ФАКТЫ
═══════════════════════════════════════
Перечитай текст и найди факты, которых НЕТ ни в датированном списке, ни в недатированном:
- Семейное происхождение (предки, родители, братья/сёстры)
- Рождение КАЖДОГО ребёнка отдельным фактом (имя, год, судьба)
- Смерти близких людей (родители, дети, супруги)
- Конкретные произведения, публикации, книги (каждое отдельно)
- Аресты, суды, ссылки, конфликты с властями
- Конкретные эпизоды (встречи, инциденты, выступления)
- Организации, союзы, общества, в которых участвовал
- Реакции на внешние события (восстания, войны, революции)
- КАЖДАЯ награда, звание, членство в академиях ОТДЕЛЬНЫМ фактом
- Научные открытия как конкретные датированные события
- Поворотные моменты: книги, встречи, события, изменившие жизнь
- Увлечения и хобби

ФОРМАТ ОТВЕТА — единый JSON-массив (датированные старые + новые факты вместе):
[
  {"year": 1828, "text": "Факт", "category": "family", "sphere": "family"},
  ...
]

ПОЛЯ
- year: число (год). ОБЯЗАТЕЛЬНО определи год для каждого факта. null — только если абсолютно невозможно.
- text: строка, 5-15 слов.
- category: одно из: birth, education, move, publication, career, family, friends, health, conflict, award, project, death, other
- sphere: одно из: education, career, creativity, family, health, friends, place, finance, hobby, other

ПРАВИЛА
- НЕ повторяй уже датированные факты из списка выше — даже перефразированные.
- Один факт = одно конкретное событие.
- НЕ выдумывай факты, которых нет в тексте.

ТЕКСТ СТАТЬИ
${params.extract}

Верни ТОЛЬКО компактный JSON-массив, без markdown, без пояснений.`;
}

export function buildBiographyFactRankingPrompt(params: {
  subjectName: string;
  facts: Array<{ index: number; year: number; labelHint: string; details: string; sphere: string }>;
}) {
  const factsBlock = params.facts
    .map((f) => `${f.index}\t${f.year}\t${f.sphere}\t${f.labelHint}\t${f.details}`)
    .join('\n');

  return `Ты — редактор биографических таймлайнов. Тебе дан список фактов о **${params.subjectName}**.

ЗАДАЧА
Оцени каждый факт по шкале 1–5 для включения в визуальный таймлайн жизненного пути.

КРИТЕРИИ ОЦЕНКИ
5 — переломный момент, без которого невозможно понять жизнь (рождение, смерть, главные произведения/достижения, духовный перелом, ключевые отношения)
4 — важное событие, формирующее нарратив (образование, переезды, карьерные повороты, конфликты)
3 — заметное событие, дополняющее картину (публикации, награды, дружбы)
2 — второстепенный факт, можно опустить
1 — тривиальный или дублирующий факт

ПРАВИЛА
- Из ~${params.facts.length} фактов поставь оценку 5 не более чем 15 фактам.
- Оценки 4-5 должны покрывать всю жизнь: детство, становление, зрелость, поздние годы.
- Не ставь оценку 5 нескольким фактам об одном и том же событии.
- Оценивай каждый факт независимо, без оглядки на порядок в списке.

ФАКТЫ
INDEX\tYEAR\tSPHERE\tLABEL\tDETAILS
${factsBlock}

ФОРМАТ ОТВЕТА
Верни ТОЛЬКО строки в формате: INDEX<TAB>SCORE
Одна строка на факт. Без заголовков, пояснений и markdown.`;
}

export function buildBiographyCompositionPrompt(params: {
  subjectName: string;
  birthYear: number;
  deathYear: number | null;
  facts: Array<{
    index: number;
    year: number;
    details: string;
    themes: string;
    people: string;
    importance: string;
  }>;
}) {
  const factsBlock = params.facts
    .map((f) => `${f.index}\t${f.year}\t${f.importance}\t${f.themes}\t${f.people}\t${f.details}`)
    .join('\n');

  const lifespan = (params.deathYear ?? params.birthYear + 80) - params.birthYear;
  const targetMainEvents = Math.max(10, Math.round(lifespan / 5 * 2));

  return `Ты — нарратолог и биограф. Тебе дан список из ${params.facts.length} фактов о **${params.subjectName}** (${params.birthYear}–${params.deathYear ?? '?'}).

ЗАДАЧА
Распредели ВСЕ факты по структуре визуального таймлайна:
1. **Главная линия** — ~${targetMainEvents} ключевых точек невозврата, после которых жизнь пошла по другому пути.
2. **Сюжетные ветки** — все остальные факты, сгруппированные в нарративные арки.

ЧТО ТАКОЕ ГЛАВНАЯ ЛИНИЯ
Главная линия — это скелет биографии «крупными мазками». На ней ТОЛЬКО поворотные события:
- Рождение и смерть (факт рождения — ТОЛЬКО на главной линии, НЕ дублируй его в ветках)
- Первые аресты, побеги, переломные решения
- Ключевые назначения и снятия с постов
- Моменты, после которых траектория жизни необратимо меняется
НЕ ставь на главную линию: детали, развитие сюжетов, второстепенные эпизоды.
Равномерно покрывай всю жизнь (~2 события на 5 лет).

ЧТО ТАКОЕ СЮЖЕТНАЯ ВЕТКА
Ветка — это НЕ категория, а ИСТОРИЯ с началом, развитием и концом.
Признаки хорошей ветки:
- Общий предмет, персона или проект: «Отношения с X», «Работа над Y», «Жизнь в Z»
- Хронологическая связность (без разрывов >10 лет, иначе это две ветки)
- Внутренняя динамика: начало → развитие → кульминация → завершение

ТИПЫ ВЕТОК (примеры, не ограничивай себя ими):
- Отношения с конкретным человеком (ментор, соратник, антагонист, супруг)
- Творческий проект или серия публикаций
- Институциональный сюжет (связь с организацией)
- Семейный сюжет (брак, дети) — каждый брак = отдельная ветка
- Географический сюжет (жизнь в месте, серия перемещений)
- Длительный конфликт или противостояние

ПРАВИЛА
- Каждый факт попадает РОВНО в одно место: либо на главную линию, либо в одну ветку.
- НИКОГДА не выбрасывай факты. Все ${params.facts.length} фактов должны быть распределены.
- Ветка содержит минимум 3 факта. Если меньше — присоедини к ближайшей по смыслу.
- Несколько веток могут иметь одну сферу (например, две ветки career: «Армия» и «Борьба за власть»).
- Имя ветки — конкретное: не «Семья», а «Семья с Седовой и дети». Не «Карьера», а «Строительство Красной армии».

ФАКТЫ
INDEX\tYEAR\tIMPORTANCE\tTHEMES\tPEOPLE\tDETAILS
${factsBlock}

ФОРМАТ ОТВЕТА
Верни ТОЛЬКО JSON (без markdown, без пояснений):
{
  "mainLine": [index, index, ...],
  "branches": [
    {"name": "Название ветки", "sphere": "одна из: education/career/creativity/family/health/friends/place/finance/hobby/other", "facts": [index, index, ...]},
    ...
  ]
}`;
}

export function buildBiographyFactEnrichmentPrompt(params: {
  subjectName: string;
  facts: Array<{ index: number; year: number; details: string }>;
}) {
  const factsBlock = params.facts
    .map((f) => `${f.index}\t${f.year}\t${f.details}`)
    .join('\n');

  return `Ты — редактор биографических таймлайнов. Тебе дан список фактов о **${params.subjectName}**.

ЗАДАЧА
Для каждого факта определи:
1. **themes** — одну или две темы из списка ниже (основная тема первой)
2. **people** — упомянутые в факте имена людей (кроме самого ${params.subjectName}). Если нет — пустой список.
3. **shortLabel** — назывной заголовок факта, до 25 символов. Без глаголов. Конкретно и различающе. Примеры: «Побег из ссылки», «Брестский мир», «Убийство в Мексике», «Председатель Петросовета».

ДОПУСТИМЫЕ ТЕМЫ (ровно эти значения, без вариаций):
- upbringing_mentors — воспитание, наставники, ранние влияния
- education — образование, учёба, школы, университеты
- friends_network — друзья, соратники, круг общения
- romance — любовь, брак, романтические отношения
- family_household — семья, родители, быт, происхождение
- children — дети, потомство
- travel_moves_exile — переезды, путешествия, ссылки, эмиграция
- service_career — карьера, должности, назначения, служба
- creative_work — публикации, книги, статьи, журналистика, творчество
- conflict_duels — конфликты, аресты, суды, дуэли, покушения, войны
- losses — потери близких, смерти соратников
- politics_public_pressure — политическая деятельность, идеология, партийная борьба, общественное давление
- health — здоровье, болезни
- legacy — наследие, посмертное влияние, увековечение

ПРАВИЛА
- Каждому факту присвой 1-2 темы. Первая — основная.
- people — только конкретные персоны, упомянутые в тексте факта (фамилии). Не добавляй людей, которых нет в тексте.
- Рождение → family_household. Смерть → conflict_duels (если насильственная) или losses.
- Политические события (партия, голосование, оппозиция) → politics_public_pressure.
- Назначения на посты → service_career.
- Аресты, ссылки с элементом перемещения → conflict_duels + travel_moves_exile.
- Публикации → creative_work.

ФАКТЫ
INDEX\tYEAR\tDETAILS
${factsBlock}

ФОРМАТ ОТВЕТА
Верни ТОЛЬКО строки в формате: INDEX<TAB>THEME1[,THEME2]<TAB>PERSON1,PERSON2 (или пусто)<TAB>shortLabel
Одна строка на факт. Без заголовков, пояснений и markdown.`;
}
