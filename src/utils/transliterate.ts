/**
 * Утилиты для транслитерации кириллицы в латиницу
 * Используется для генерации URL-safe ID из русских названий
 */

const CYRILLIC_TO_LATIN: Record<string, string> = {
  'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd',
  'е': 'e', 'ё': 'yo', 'ж': 'zh', 'з': 'z', 'и': 'i',
  'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm', 'н': 'n',
  'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't',
  'у': 'u', 'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch',
  'ш': 'sh', 'щ': 'sch', 'ъ': '', 'ы': 'y', 'ь': '',
  'э': 'e', 'ю': 'yu', 'я': 'ya',
};

/**
 * Транслитерирует текст из кириллицы в латиницу
 */
export function transliterate(text: string): string {
  return text
    .toLowerCase()
    .split('')
    .map(char => CYRILLIC_TO_LATIN[char] ?? char)
    .join('');
}

/**
 * Генерирует URL-safe ID из названия занятия
 * - Транслитерирует кириллицу
 * - Заменяет пробелы и спецсимволы на дефисы
 * - Убирает повторяющиеся дефисы
 * - Ограничивает длину
 */
export function generateLessonId(title: string): string {
  return transliterate(title)
    .replace(/[^a-z0-9]+/g, '-')  // заменяем спецсимволы на дефис
    .replace(/-+/g, '-')          // убираем повторяющиеся дефисы
    .replace(/^-|-$/g, '')        // убираем дефисы по краям
    .slice(0, 50);                // ограничиваем длину
}
