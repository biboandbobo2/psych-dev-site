import { fireEvent, render, screen } from '@testing-library/react';
import { describe, expect, it, vi } from 'vitest';
import { VideoTranscriptPanel } from './VideoTranscriptPanel';

describe('VideoTranscriptPanel', () => {
  it('перематывает видео по клику на таймкод транскрипта', () => {
    const handleTimestampClick = vi.fn();

    render(
      <VideoTranscriptPanel
        error={null}
        isChecking={false}
        isLoading={false}
        onTimestampClick={handleTimestampClick}
        transcript={{
          youtubeVideoId: 'video-1',
          version: 1,
          source: 'youtube',
          language: 'ru',
          languageName: 'Russian',
          isAutoGenerated: true,
          fetchedAt: '2026-03-09T10:00:00.000Z',
          durationMs: 120000,
          fullText: 'Первый фрагмент',
          segments: [
            {
              index: 0,
              startMs: 32000,
              endMs: 36000,
              durationMs: 4000,
              text: 'Первый фрагмент',
            },
          ],
        }}
      />
    );

    fireEvent.click(screen.getByRole('button', { name: '00:32' }));

    expect(handleTimestampClick).toHaveBeenCalledWith(32000);
  });

  it('центрирует transcript по актуальному времени видео при открытии панели', () => {
    const scrollTo = vi.fn();
    const { container, rerender } = render(
      <VideoTranscriptPanel
        error={null}
        focusTimeMs={null}
        isChecking={false}
        isLoading={false}
        onTimestampClick={() => {}}
        transcript={{
          youtubeVideoId: 'video-1',
          version: 1,
          source: 'youtube',
          language: 'ru',
          languageName: 'Russian',
          isAutoGenerated: true,
          fetchedAt: '2026-03-09T10:00:00.000Z',
          durationMs: 120000,
          fullText: 'Первый фрагмент Второй фрагмент',
          segments: [
            {
              index: 0,
              startMs: 32000,
              endMs: 36000,
              durationMs: 4000,
              text: 'Первый фрагмент',
            },
            {
              index: 1,
              startMs: 47000,
              endMs: 52000,
              durationMs: 5000,
              text: 'Второй фрагмент',
            },
          ],
        }}
      />
    );

    const scrollContainer = container.querySelector('.overflow-y-auto') as HTMLDivElement;
    const focusedCard = container.querySelector('[data-start-ms="47000"]') as HTMLDivElement;

    Object.defineProperty(scrollContainer, 'clientHeight', {
      configurable: true,
      value: 400,
    });
    Object.defineProperty(focusedCard, 'offsetTop', {
      configurable: true,
      value: 260,
    });
    Object.defineProperty(focusedCard, 'offsetHeight', {
      configurable: true,
      value: 80,
    });
    scrollContainer.scrollTo = scrollTo;

    rerender(
      <VideoTranscriptPanel
        error={null}
        focusTimeMs={49000}
        isChecking={false}
        isLoading={false}
        onTimestampClick={() => {}}
        transcript={{
          youtubeVideoId: 'video-1',
          version: 1,
          source: 'youtube',
          language: 'ru',
          languageName: 'Russian',
          isAutoGenerated: true,
          fetchedAt: '2026-03-09T10:00:00.000Z',
          durationMs: 120000,
          fullText: 'Первый фрагмент Второй фрагмент',
          segments: [
            {
              index: 0,
              startMs: 32000,
              endMs: 36000,
              durationMs: 4000,
              text: 'Первый фрагмент',
            },
            {
              index: 1,
              startMs: 47000,
              endMs: 52000,
              durationMs: 5000,
              text: 'Второй фрагмент',
            },
          ],
        }}
      />
    );

    expect(scrollTo).toHaveBeenCalledWith({
      behavior: 'auto',
      top: 100,
    });
    expect(screen.getByText('Второй фрагмент').closest('[data-start-ms="47000"]')).toHaveClass(
      'border-accent'
    );
  });
});
